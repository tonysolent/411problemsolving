direction = input("Towards which direction should I paint (up, down, left or right)?\n")
if direction == "up":
    print("I am painting in the upward direction!\n")
elif direction == "down":
    print("I am painting in the downbird direction!\n")
elif direction == "left":
    print("I am painting in the leftward direction!\n")
elif direction == "right":
    print("I am painting in the rightward direction!\n")