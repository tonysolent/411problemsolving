direction = input ("Towards which direction should I paint (up, down, left or right)?")
if direction == "up":
    print("I am painting in the upward direction!")
elif direction == "down":
    print("I am painting in downward direction")
elif direction == "left":
    print("I am painting in left direction")
elif direction == "right":
    print("I am painting in right direction")
else:
    print("That is the wrong direction")
